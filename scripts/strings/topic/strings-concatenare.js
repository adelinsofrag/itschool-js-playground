var greeting = "Hello";
var name = "John";

// Utilizarea operatorului de concatenare
var message = greeting + ", " + name;
console.log(message); // Output: "Hello, John"

// Utilizarea metodei concat()
var message2 = greeting.concat(", ", name);
console.log(message2); // Output: "Hello, John"